package typecasting;

public class Primitive {
	
	public static void main(String[] args) {
		
		//Widening Casting
		int a=5;
		float b=a;
		System.out.println("widening casting :"+b);
		
		//Narrowing Casting 
		
		double c=9.78;
		int d=(int)c;
		System.out.println("Narrowing casting:"+d);
		
	}

}
